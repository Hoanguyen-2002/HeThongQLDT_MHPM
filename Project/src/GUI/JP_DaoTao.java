    public void BiddingHeDaoTao() {
        lstHeDaoTao = dLL_HeDaoTao.getAllHeDaoTao();
        Vector Column = new Vector();
        Column.add("Mã Hệ Đào Tạo");
        Column.add("Tên Hệ Đào Tạo");
        Vector data = new Vector();
        try {
            for (DBO_HE_DAO_TAO item : lstHeDaoTao) {
                Vector row = new Vector();
                row.add(item.getMaHeDaoTao());
                row.add(item.getTenHeDaoTao());
                data.add(row);
            }
        } catch (Exception e) {
            method.showMessegaWa("Error for biding HeDaoTao : " + e.toString());
        }
        tblDaoTao.setAutoCreateRowSorter(true);
        dtm = new DefaultTableModel(data, Column){
            public boolean isCellEditable(int rowCount, int columnCount){
                return false;
            }
        };
        tblDaoTao.setModel(dtm);
    }

