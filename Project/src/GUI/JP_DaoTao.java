    private void btnDeleteDaoTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteDaoTaoActionPerformed
        int yes = JOptionPane.showConfirmDialog(null, "Bạn Muốn Xóa Dữ Liệu ?", "Thông Báo", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (yes == JOptionPane.YES_OPTION) {
            String ma = txtMaHeDaoTao.getText();
            if (ma.isEmpty()) {
                method.showMessegaNo("MaHeDaoTao is Empty");
            } else if (dLL_HeDaoTao.Delete(ma)) {
                method.showMessegaNo("Xóa Thành Công !");
                BiddingHeDaoTao();
                res();
            } else {
                method.showMessegaNo("Xóa Thất Bại !");
            }
        }
    }//GEN-LAST:event_btnDeleteDaoTaoActionPerformed

    private void btnAddDaoTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDaoTaoActionPerformed
        String ma = txtMaHeDaoTao.getText();
        txtMaHeDaoTao.requestFocus();
        txtMaHeDaoTao.setEnabled(true);
        String ten = txtTenHeDaoTao.getText();
        lstHeDaoTao = dLL_HeDaoTao.Check_HeDaoTao(ma);
        if (ma.isEmpty() || ten.isEmpty()) {
            method.showMessegaNo("Bạn Cần Nhập Đầy Đủ Thông Tin !");
        } else if (lstHeDaoTao.size() > 0) {
            method.showMessegaWa("Đã Tồn Tại Hệ Đào Tạo Này, Bạn Không Thể Thêm Vào Nữa");
            res();
        } else {
            DBO_HE_DAO_TAO obj = new DBO_HE_DAO_TAO();
            obj.setMaHeDaoTao(ma);
            obj.setTenHeDaoTao(ten);
            if (dLL_HeDaoTao.Insert(obj)) {
                method.showMessegaNo("Thêm Thành Công !");
                BiddingHeDaoTao();
            } else {
                method.showMessegaNo("Thêm Thất Bại !");
            }
        }
    }//GEN-LAST:event_btnAddDaoTaoActionPerformed

    private void btnUpdateDaoTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDaoTaoActionPerformed
        String ma = txtMaHeDaoTao.getText();
        String ten = txtTenHeDaoTao.getText();
        if (ma.isEmpty() || ten.isEmpty()) {
            method.showMessegaNo("Bạn Cần Nhập Đầy Đủ Thông Tin !");
        } else {
            DBO_HE_DAO_TAO obj = new DBO_HE_DAO_TAO();
            obj.setMaHeDaoTao(ma);
            obj.setTenHeDaoTao(ten);
            if (dLL_HeDaoTao.Update(obj, dk)) {
                method.showMessegaNo("Cập Nhật Thành Công !");
                BiddingHeDaoTao();
                res();
            } else {
                method.showMessegaNo("Cập Nhật Thất Bại !");
            }
        }
    }//GEN-LAST:event_btnUpdateDaoTaoActionPerformed

